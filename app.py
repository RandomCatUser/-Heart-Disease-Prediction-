import streamlit as st
import pandas as pd
import plotly.graph_objects as go

from utils import load_model
from config import MODEL_PATH

# -----------------------------
# Page Configuration
# -----------------------------

st.set_page_config(
    page_title="Heart Disease AI",
    page_icon="❤️",
    layout="wide"
)

# -----------------------------
# Load Model
# -----------------------------

@st.cache_resource
def get_model():
    return load_model(MODEL_PATH)

model = get_model()

# -----------------------------
# Sidebar
# -----------------------------

st.sidebar.title("❤️ Heart Disease AI")

st.sidebar.info(
"""
AI-powered Heart Disease Risk Predictor

This application predicts the probability
of heart disease based on clinical data.

⚠ Educational use only.
"""
)

# -----------------------------
# Header
# -----------------------------

st.title("❤️ Heart Disease Prediction")

st.write(
"Enter patient information below."
)

# -----------------------------
# Input Columns
# -----------------------------

col1, col2 = st.columns(2)

with col1:

    age = st.slider("Age", 20, 100, 50)

    sex = st.selectbox(
        "Sex",
        ["Female", "Male"]
    )

    cp = st.selectbox(
        "Chest Pain Type",
        [0,1,2,3]
    )

    trestbps = st.number_input(
        "Resting Blood Pressure",
        80,
        250,
        120
    )

    chol = st.number_input(
        "Cholesterol",
        100,
        700,
        200
    )

    fbs = st.selectbox(
        "Fasting Blood Sugar >120",
        ["No","Yes"]
    )

with col2:

    restecg = st.selectbox(
        "Resting ECG",
        [0,1,2]
    )

    thalach = st.slider(
        "Maximum Heart Rate",
        60,
        220,
        150
    )

    exang = st.selectbox(
        "Exercise Induced Angina",
        ["No","Yes"]
    )

    oldpeak = st.slider(
        "Old Peak",
        0.0,
        6.5,
        1.0
    )

    slope = st.selectbox(
        "Slope",
        [0,1,2]
    )

    ca = st.selectbox(
        "Major Vessels",
        [0,1,2,3,4]
    )

    thal = st.selectbox(
        "Thal",
        [0,1,2,3]
    )

# -----------------------------
# Predict
# -----------------------------

if st.button("Predict Risk", use_container_width=True):

    patient = pd.DataFrame([{

        "age": age,
        "sex": 1 if sex=="Male" else 0,
        "cp": cp,
        "trestbps": trestbps,
        "chol": chol,
        "fbs": 1 if fbs=="Yes" else 0,
        "restecg": restecg,
        "thalach": thalach,
        "exang": 1 if exang=="Yes" else 0,
        "oldpeak": oldpeak,
        "slope": slope,
        "ca": ca,
        "thal": thal

    }])

    prediction = model.predict(patient)[0]

    probability = model.predict_proba(patient)[0][1]

    st.divider()

    if prediction == 1:
        st.error("⚠ High Risk")
    else:
        st.success("✅ Low Risk")

    st.metric(
        "Risk Probability",
        f"{probability*100:.1f}%"
    )

    # Gauge Chart

    fig = go.Figure(go.Indicator(

        mode="gauge+number",

        value=probability*100,

        title={"text":"Heart Disease Risk"},

        gauge={
            "axis":{"range":[0,100]},
            "bar":{"color":"red"},
            "steps":[
                {"range":[0,30],"color":"green"},
                {"range":[30,60],"color":"yellow"},
                {"range":[60,100],"color":"red"}
            ]
        }

    ))

    st.plotly_chart(
        fig,
        use_container_width=True
    )

    st.subheader("AI Recommendation")

    if probability < 0.30:

        st.success("""
Continue healthy habits.

• Exercise regularly

• Eat balanced meals

• Maintain healthy weight

• Annual checkups
""")

    elif probability < 0.60:

        st.warning("""
Moderate Risk

• Improve diet

• Increase physical activity

• Monitor blood pressure

• Schedule a medical checkup
""")

    else:

        st.error("""
High Risk

• Consult a doctor promptly

• Consider ECG

• Monitor cholesterol

• Follow professional medical advice
""")

st.divider()

st.caption(
"This prediction is generated by a machine learning model for educational purposes only. "
"It does not diagnose heart disease or heart attacks. Always consult a qualified healthcare professional for medical advice."
)