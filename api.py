from fastapi import FastAPI
from pydantic import BaseModel
import pandas as pd

from utils import load_model
from config import MODEL_PATH

# -----------------------------
# Load Model
# -----------------------------

model = load_model(MODEL_PATH)

# -----------------------------
# Create API
# -----------------------------

app = FastAPI(
    title="Heart Disease AI API",
    description="Heart Disease Risk Prediction API",
    version="1.0.0"
)

# -----------------------------
# Input Schema
# -----------------------------

class Patient(BaseModel):

    age: int
    sex: int
    cp: int
    trestbps: float
    chol: float
    fbs: int
    restecg: int
    thalach: float
    exang: int
    oldpeak: float
    slope: int
    ca: int
    thal: int


# -----------------------------
# Home Route
# -----------------------------

@app.get("/")
def home():

    return {
        "message": "Heart Disease AI API",
        "status": "Running"
    }


# -----------------------------
# Health Check
# -----------------------------

@app.get("/health")
def health():

    return {
        "status": "OK"
    }


# -----------------------------
# Prediction Route
# -----------------------------

@app.post("/predict")
def predict(patient: Patient):

    df = pd.DataFrame([patient.dict()])

    prediction = int(model.predict(df)[0])

    probability = float(model.predict_proba(df)[0][1])

    risk = "High Risk" if prediction == 1 else "Low Risk"

    if probability < 0.30:
        recommendation = (
            "Maintain a healthy lifestyle and continue regular medical checkups."
        )
    elif probability < 0.60:
        recommendation = (
            "Consider consulting a healthcare professional and monitor cardiovascular health."
        )
    else:
        recommendation = (
            "Consult a qualified healthcare professional promptly for further evaluation."
        )

    return {
        "prediction": risk,
        "probability": round(probability, 4),
        "confidence": round(max(probability, 1 - probability), 4),
        "recommendation": recommendation,
        "disclaimer": (
            "This prediction is generated by a machine learning model for "
            "educational purposes only and is not a medical diagnosis."
        )
    }